#include "stdafx.h"

#include "smShare.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize program resources.

void main_initialize()
{
   printf("ShareLibACMPhpExt********************************************BEGIN\n");

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Initialize print facility.

   // Initialize print.
   Prn::resetPrint();
   Prn::useConsole(3);
   Prn::initializePrint();

   // Initialize print filters.
   Prn::setFilter(Prn::View11, true,  1);
   Prn::setFilter(Prn::View12, false, 1);
   Prn::setFilter(Prn::View13, false, 1);
   Prn::setFilter(Prn::View14, false, 1);

   Prn::setFilter(Prn::View31, true,  3);
   Prn::setFilter(Prn::View32, false, 3);
   Prn::setFilter(Prn::View33, false, 3);
   Prn::setFilter(Prn::View34, false, 3);

   Prn::print(Prn::View31, "ShareLibACMPhpExt********************************************BEGIN");

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // initialize.

   // Initialize shared memory.
   SM::initializeShare();
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Finalize program resourcs.

void main_finalize()
{
   Prn::print(Prn::View31, "ShareLibACMPhpExt********************************************END");

   // Finalize shared memory.
   SM::finalizeShare();

   // Finalize print facility.
   Prn::finalizePrint();

   // Done.
   printf("\n");
   printf("ShareLibACMPhpExt********************************************END\n");
}

